package org.tenece.web.data;

import java.util.Date;

/**
 *
 * @author jeffry.amachree
 */
public class EmployeeAttendance extends BaseData {
    
    private int id;
    private int employeeId;
    private Date actionDate;
    private String actionType;
    private String actionTime;
    private String device;
    private String punchError;
    private String custom1;
    private String custom2;

    private String employeeName;
    private int err;
    
    /** Creates a new instance of EmployeeAttendance */
    public EmployeeAttendance() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public Date getActionDate() {
        return actionDate;
    }

    public void setActionDate(Date actionDate) {
        this.actionDate = actionDate;
    }

    public String getActionType() {
        return actionType;
    }

    public void setActionType(String actionType) {
        this.actionType = actionType;
    }

    public String getActionTime() {
        return actionTime;
    }

    public void setActionTime(String actionTime) {
        this.actionTime = actionTime;
    }

    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }

    public String getPunchError() {
        return punchError;
    }

    public void setPunchError(String punchError) {
        this.punchError = punchError;
    }

    public String getCustom1() {
        return custom1;
    }

    public void setCustom1(String custom1) {
        this.custom1 = custom1;
    }

    public String getCustom2() {
        return custom2;
    }

    public void setCustom2(String custom2) {
        this.custom2 = custom2;
    }

    /**
     * @return the employeeName
     */
    public String getEmployeeName() {
        return employeeName;
    }

    /**
     * @param employeeName the employeeName to set
     */
    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    /**
     * @return the err
     */
    public int getErr() {
        return err;
    }

    /**
     * @param err the err to set
     */
    public void setErr(int err) {
        this.err = err;
    }
    
}
