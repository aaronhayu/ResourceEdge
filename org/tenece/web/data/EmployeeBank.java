package org.tenece.web.data;

/**
 *
 * @author jeffry.amachree
 */

public class EmployeeBank extends BaseData {
    private int id;
    private int employeeId;
    private String bankName;
    private String branch;
    private String accountName;
    private String accountType;
    private String accountNumber;
    private int policyId;

    //for transaction tables only
    private long transactionId;
    private String eStatus;
    
    /** Creates a new instance of EmployeeBank */
    public EmployeeBank() {
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getAccountType() {
        return accountType;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    /**
     * @return the transactionId
     */
    public long getTransactionId() {
        return transactionId;
    }

    /**
     * @param transactionId the transactionId to set
     */
    public void setTransactionId(long transactionId) {
        this.transactionId = transactionId;
    }

    /**
     * @return the eStatus
     */
    public String geteStatus() {
        return eStatus;
    }

    /**
     * @param eStatus the eStatus to set
     */
    public void seteStatus(String eStatus) {
        this.eStatus = eStatus;
    }

    public String getAccountTypeDescription(){
        if(this.accountType != null && accountType.trim().equals("20")){
            return "Checking Account";
        }
        if(this.accountType != null && accountType.trim().equals("10")){
            return "Savings Account";
        }
        return "N/A";
    }

    /**
     * @return the policyId
     */
    public int getPolicyId() {
        return policyId;
    }

    /**
     * @param policyId the policyId to set
     */
    public void setPolicyId(int policyId) {
        this.policyId = policyId;
    }
}
